<h1>Formål</h1>
<p>ToldJS er en hjemmeside som har til formål at forenkle fortoldningsprocessen. Vores mål er at gøre det billigere at bestille vare fra udlandet, og undgå urimelige transportgebyrer. </p>
<h1>Kontakt</h1>
<p>ToldJS kan kontaktes pr. email på support@toldjs.dk hvor vi bestræber os på at besvare hurtigst muligt</p>
